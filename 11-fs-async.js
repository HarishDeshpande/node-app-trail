const { readFile, writeFile } = require("fs");

// CALLBACK FUNCTION
// we'll run callback when we are done
readFile("./content/first.txt", "utf-8", (err, result) => {
  if (err) {
    console.log(err);
    return;
  }
  const first = result;
  //   reading second file
  readFile("./content/second.txt", "utf-8", (err, result) => {
    if (err) {
      console.log(err);
      return;
    }
    const second = result;
    //   writing the file
    writeFile(
      "./content/result-async.txt",
      `Here is result ${first}, ${second}`,
      (err, result) => {
        if (err) {
          console.log(err);
          return;
        }
        console.log(result);
      }
    );
  });
});
